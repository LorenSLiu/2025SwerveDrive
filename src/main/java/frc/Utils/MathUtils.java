package frc.Utils;

import static edu.wpi.first.units.Units.*;

public class MathUtils {
    public static boolean isWithinTolerance(double value, double target, double tolerance) {
        return Math.abs(value - target) <= tolerance;
    }

    public static double elevatorPostitionRotationToMeters(double rotation){
        return (rotation / frc.robot.Constants.ElevatorConstants.GEAR_RATIO) * Math.PI * frc.robot.Constants.ElevatorConstants.PULLEY_DIAMETER.in(Meters);
    }

    public static double elevatorMetersToRotation(double meters){
        return (frc.robot.Constants.ElevatorConstants.GEAR_RATIO * meters) / (Math.PI * frc.robot.Constants.ElevatorConstants.PULLEY_DIAMETER.in(Meters));
    }
    
}
